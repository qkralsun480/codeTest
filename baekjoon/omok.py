dx = [0, 1, 1, 1]
dy = [1, 1, 0, -1] 

def check():
    for i in range(19):
        for j in range(19):
            visit = [[0]*19 for _ in range(19)]
            if board[i][j] > 0: #아직 확인해보지 않은 칸이라면
                # 방문 확인
                visit[i][j] = 1
                result = [(i, j)]

                for k in range(4):
                    cnt = 1 # 연속되는 바둑알을 세기 위한 변수
                    nx = i + dx[k]
                    ny = j + dy[k]
                
                    while 0<=nx<19 and 0<=ny<19 and visit[nx][ny] == 0 and board[i][j] == board[nx][ny] : #다음칸에 색이 같은 알이 있다면
                        cnt += 1
                        visit[nx][ny] = 1
                        result.append((nx, ny))
                        nx += dx[k]
                        ny += dy[k]

                    if cnt == 5:
                        bx = i - dx[k]
                        by = j - dy[k]
                        # 여섯개가 연속이 되는경우
                        if 0<=bx<19 and 0<=by<19 and board[bx][by] == board[i][j]:
                            continue
                        else:
                            return board[i][j], result, k # 색,좌표리스트
    return 0, 0, 0

